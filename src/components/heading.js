import React from 'react';
import Button from '@mui/material/Button';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import Fade from '@mui/material/Fade';
import { styled, alpha } from '@mui/material/styles';
import FormGroup from '@mui/material/FormGroup';
import { useTasks } from '../TaskProvider'
import FormControlLabel from '@mui/material/FormControlLabel';
import {  ReactComponent as Vector} from "./Vector.svg"
import {IOSSwitch} from "./styledSwitch"

const StyledMenu = styled((props) => (
    <Menu
      elevation={0}
      anchorOrigin={{
        vertical: 'bottom',
        horizontal: 'right',
      }}
      transformOrigin={{
        vertical: 'top',
        horizontal: 'right',
      }}
      {...props}
    />
  ))(({ theme }) => ({
    '& .MuiPaper-root': {
      borderRadius: 6,
      marginTop: theme.spacing(1),
      minWidth: 180,
      color:
        theme.palette.mode === 'light' ? 'rgb(55, 65, 81)' : theme.palette.grey[300],
      boxShadow:
        'rgb(255, 255, 255) 0px 0px 0px 0px, rgba(0, 0, 0, 0.05) 0px 0px 0px 1px, rgba(0, 0, 0, 0.1) 0px 10px 15px -3px, rgba(0, 0, 0, 0.05) 0px 4px 6px -2px',
      '& .MuiMenu-list': {
        padding: '4px 0',
      },
      '& .MuiMenuItem-root': {
        '& .MuiSvgIcon-root': {
          fontSize: 18,
          color: theme.palette.text.secondary,
          marginRight: theme.spacing(1.5),
        },
        '&:active': {
          backgroundColor: alpha(
            theme.palette.primary.main,
            theme.palette.action.selectedOpacity,
          ),
        },
      },
    },
  }));

const Heading = () => {
    const { toggleNews, newsDisplay } = useTasks()
  const [anchorEl, setAnchorEl] = React.useState(null);
  const open = Boolean(anchorEl);
  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

    return (
        <div style={{display: "flex", alignItems: "center"}}>
            <h1 style={{color: "#ffffff"}}>TODO LIST</h1>
            <div style={{marginLeft: "auto", position: "relative"}}>
            
            <Button
                id="fade-button"
                aria-controls={open ? 'fade-menu' : undefined}
                aria-haspopup="true"
                aria-expanded={open ? 'true' : undefined}
                onClick={handleClick}
            >
                <Vector />
            </Button>
           
            <StyledMenu
                id="fade-menu"
                MenuListProps={{
                'aria-labelledby': 'fade-button',
                }}
                anchorEl={anchorEl}
                open={open}
                onClose={handleClose}
                TransitionComponent={Fade}
                
            >
                <MenuItem onClick={handleClose} > 
                    <FormGroup>
                        <FormControlLabel
                        control={<IOSSwitch sx={{ m: 1 }} checked={toggleNews} onChange={newsDisplay}/>}
                        label={toggleNews ? "Hide Newsletters": "Show Newsletters"}
                        />
                    </FormGroup>
                </MenuItem>
                
            </StyledMenu>
            </div>
            
        </div>
    );
}



export default Heading;